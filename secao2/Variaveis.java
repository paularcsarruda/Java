public class Variaveis {
    public static void main(String[] args) {
        int age = 37;
        String name = "Paula Arruda";
        double height = 1.58;
        boolean pet = true;


        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Pet: " + pet);
    }
}
