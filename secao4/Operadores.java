package secao4;

public class Operadores {
    public static void main(String[] args) {
        /*
         * == -> igual a
         * != -> diferente de
         *  > -> maior que
         *  < -> menor que
         * >= -> maior ou igual
         * <= -> menor ou igual
         */

         int x = 10;

         System.out.println(x == 10);
         System.out.println(x > 80);
         System.out.println(x != 80);

    }
        
}
